package com.example.sharekhancalc;

public class Firebase_Constituencies
{

    String State;
    String Date;
    String Constituency;
    String Type;

    public String getState() {
        return State;
    }

    public void setState(String state) {
        State = state;
    }

    public String getDate() {
        return Date;
    }

    public void setDate(String date) {
        Date = date;
    }

    public String getConstituency() {
        return Constituency;
    }

    public void setConstituency(String constituency) {
        Constituency = constituency;
    }

    public String getType() {
        return Type;
    }

    public void setType(String type) {
        Type = type;
    }

    public String getConstituency_Number() {
        return Constituency_Number;
    }

    public void setConstituency_Number(String constituency_Number) {
        Constituency_Number = constituency_Number;
    }

    public String getIsAdjourned() {
        return IsAdjourned;
    }

    public void setIsAdjourned(String isAdjourned) {
        IsAdjourned = isAdjourned;
    }

    String Constituency_Number;
    String IsAdjourned;
}
